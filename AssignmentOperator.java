public class AssignmentOperator {

	public static void main(String[] args) {
		
		int newValue = 50;
		
		if(newValue == 50) {
			System.out.println("New value is 50");
		}
		
		boolean isCar = false;
		if(isCar == true) {
			System.out.println("Its a car");
		}else {
			System.out.println("Not a car");
		}

	}

}
