public class OperatorsOperandsAndExpressions {

	public static void main(String[] args) {
		
		char myFirstChar = 'A';
		char mySecondChar = 'B'; 
		System.out.println(myFirstChar + mySecondChar);   //Adds the value of the char
		System.out.println("" + myFirstChar + mySecondChar);   //This concatenates char
		
		int result = 5;
		result = result % 3;
		System.out.println(result);
		

	}

}
